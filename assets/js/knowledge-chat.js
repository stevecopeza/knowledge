jQuery(document).ready(function($) {
    const $chatHistory = $('#knowledge-chat-history');
    const $input = $('#knowledge-chat-input');
    const $submit = $('#knowledge-chat-submit');
    const $status = $('#knowledge-chat-status');

    // Display Connection Info
    if (knowledgeChat.ollama_url) {
        const $container = $('#knowledge-chat-container');
        const $connectionInfo = $('<div>')
            .css({
                'font-size': '11px',
                'color': '#888',
                'margin-top': '8px',
                'text-align': 'right',
                'font-style': 'italic'
            })
            .text('Using AI Provider: ' + knowledgeChat.ollama_url);
        $container.append($connectionInfo);
    }

    function appendMessage(sender, text, type, provenance) {
        const $msg = $('<div>').addClass('knowledge-chat-message').addClass('message-' + type);
        
        // Convert newlines to <br> for simple formatting
        const formattedText = text.replace(/\n/g, '<br>');
        
        let content = '<strong>' + sender + ':</strong><br>' + formattedText;

        // Append provenance if available
        if (provenance && provenance.provider_name) {
            content += '<div style="font-size: 0.8em; color: #888; margin-top: 5px; border-top: 1px dashed #ddd; padding-top: 3px;">';
            content += 'Generated by: ' + provenance.provider_name + ' (' + provenance.model + ')';
            content += '</div>';
        }

        $msg.html(content);
        $chatHistory.append($msg);
        $chatHistory.scrollTop($chatHistory[0].scrollHeight);
    }

    function askAI() {
        const question = $input.val().trim();
        const mode = $('#knowledge-chat-mode').val();
        
        if (!question) return;

        // UI Updates
        appendMessage('You', question, 'user');
        $input.val('').prop('disabled', true);
        $submit.prop('disabled', true);
        $status.text('Thinking...');

        // AJAX Request
        $.ajax({
            url: knowledgeChat.ajaxurl,
            type: 'POST',
            data: {
                action: 'knowledge_chat',
                nonce: knowledgeChat.nonce,
                question: question,
                mode: mode
            },
            success: function(response) {
                if (response.success) {
                    // Check if response data is an object with answer/provenance or just a string (legacy)
                    let answer = response.data;
                    let provenance = null;
                    
                    if (typeof response.data === 'object' && response.data.answer) {
                        answer = response.data.answer;
                        provenance = response.data.provenance;
                    }
                    
                    appendMessage('AI', answer, 'ai', provenance);
                } else {
                    appendMessage('System', 'Error: ' + response.data, 'error');
                }
            },
            error: function(xhr, status, error) {
                appendMessage('System', 'Network Error: ' + error, 'error');
            },
            complete: function() {
                $input.prop('disabled', false).focus();
                $submit.prop('disabled', false);
                $status.text('');
            }
        });
    }

    // Event Listeners
    $submit.on('click', askAI);
    
    $input.on('keypress', function(e) {
        if (e.which === 13) {
            askAI();
        }
    });
});
